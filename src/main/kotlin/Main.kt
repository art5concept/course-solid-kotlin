fun main() {
    // Posfix y Prefix incrementation
    var x = 0


    // postfix
    println("x++ = ${x++}")
    // x++ = 0

    // prefix
    println("++x = ${++x}")
    // ++x = 2


    // postfix
    println("x-- = ${x--}")
    // x-- = 2

    // prefix
    println("--x = ${--x}")
    // --x = 0


}